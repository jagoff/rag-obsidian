"""Unified in-process scheduler for obsidian-rag cron-style jobs.

Replaces ~24 individual launchd plists with a single long-running
`BackgroundScheduler` (APScheduler 3.x). Each job runs the CLI command
as a subprocess with a per-job timeout (default 25min).

Activation: ``RAG_USE_SCHEDULER=1`` — without it the legacy individual
plists are still installed by ``rag setup`` (no behaviour change).

Config file: ``~/.config/obsidian-rag/scheduler.yaml``
  Generated by ``_write_default_scheduler_yaml()`` on first run.

State table: ``rag_scheduler_runs`` in telemetry.db
  id, ts_start, ts_end, job_name, exit_code, duration_s, error_msg

CLI:
  rag scheduler          — start the daemon (blocks; KeepAlive via plist)
  rag scheduler status   — list jobs with last_run / next_run / last_status
"""

